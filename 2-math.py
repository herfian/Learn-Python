#Fundamental of Data Types
#int and float
print(type(16 + 12)) # <class 'int'>
print(type(5 - 7)) # <class 'int'>
print(type(12 / 3)) # <class 'float'>
print(type(5 * 8)) # <class 'int'>

print(11 ** 2) # 11x11 = 121
print(7 // 3) # 7:3 = 2
print(7 % 3) # sisa bagi = 1

#Math functions
print(round(3.9)) # Dibulatkan = 4
print(abs(-17)) # 17

#Operator precedence
print(20 * 8 - 9) # 151
print((4 + 1) + 2 ** 5) # 37

#complex
print(bin(6)) # 0b110
print(int('0b110', 2)) # 6